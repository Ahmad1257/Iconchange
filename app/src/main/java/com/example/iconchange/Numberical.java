package com.example.iconchange;

public class Numberical {
    int img;
    String title;


    public Numberical(int img, String title)
    {
        this.img = img;
        this.title = title;
    }

    public int getImg()
    {
        return img;
    }

    public String getTitle()
    {
        return title;
    }
}
